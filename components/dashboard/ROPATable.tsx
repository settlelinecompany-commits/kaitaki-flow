'use client'

import { useRouter } from 'next/navigation';
import Link from 'next/link';
import {
  Box,
  Button,
  Chip,
  Paper,
  Stack,
  Table,
  TableBody,
  TableCell,
  TableContainer,
  TableHead,
  TableRow,
  Tooltip,
  Typography,
} from '@mui/material';
import AddIcon from '@mui/icons-material/Add';
import { ROPAActivity } from './ropaTypes';
import { mockROPAActivities } from './ropaData';

interface ROPATableProps {
  onEditActivity: (activity: ROPAActivity) => void;
  onAddActivity: () => void;
}

const getRiskColor = (risk: string): 'success' | 'warning' | 'error' | 'default' => {
  switch (risk) {
    case 'Low':
      return 'success';
    case 'Medium':
      return 'warning';
    case 'High':
      return 'error';
    default:
      return 'default';
  }
};

const getStatusColor = (status: string): 'success' | 'warning' | 'error' | 'default' => {
  switch (status) {
    case 'Healthy':
      return 'success';
    case 'Needs Review':
      return 'warning';
    case 'Missing Data':
      return 'error';
    default:
      return 'default';
  }
};

